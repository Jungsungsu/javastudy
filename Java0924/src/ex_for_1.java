
public class ex_for_1 {

	public static void main(String[] args) {
		
		// 첫번째 for문
		for(int i = 2; i <= 9; i++) {
			System.out.println("==" + i + "단==");
			
		// 두번째 for문
		for(int j = 1; j <= 9; j++) {
			System.out.println(i + "*" + j + "=" + (i*j));
		}
		System.out.println();
		
		}
		
		
		
		
		
		
		
	}

}

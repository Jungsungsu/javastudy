
public class ex02 {

	public static void main(String[] args) {
		
		
		int[] array = new int[5];
		
		
		for(int i=0; i<array.length; i++) {
			for(int j=0; j<i; j++) {
				System.out.println();
				
			}
		}
		
		for(int i =0; i<array.length; i++) {
			for(int j =0; j>i; j++) {
				System.out.println();
			}
		}
		System.out.println();
		
		

	}

}
